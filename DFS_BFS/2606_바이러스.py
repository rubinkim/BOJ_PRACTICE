v = int(input())
e = int(input())

graph = {}
for _ in range(e):
      a, b = map(int, input().split())
      if a not in graph.keys():
            graph[a] = [b]
      else:
            graph[a].append(b)
      if b not in graph.keys():
            graph[b] = [a]
      else:
            graph[b].append(a)
            
from collections import deque

def dfs_deque(graph, start):
    q = deque([start])
    path = []
    while q:
        now = q.pop()
        if now not in path and now != start:
            path.append(now)
        for nxt in graph[now]:
            if nxt not in path:
                q.append(nxt)
    return len(path)

print(dfs_deque(graph, 1))